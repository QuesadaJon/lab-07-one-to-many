DROP TABLE IF EXISTS phones CASCADE;
DROP TABLE IF EXISTS colors;

CREATE TABLE phones (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    brand TEXT NOT NULL,
    model TEXT NOT NULL
);

CREATE TABLE colors (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  color TEXT NOT NULL,
  phone_id BIGINT REFERENCES phones(id)
)